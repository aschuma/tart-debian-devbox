#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${SCRIPT_DIR}/../shared-env.sh"

# .env file lives alongside provision.d/ (i.e. in bin/)
ENV_FILE="${SCRIPT_DIR}/../.env"

# Base64-encode .env to safely transfer special characters to the VM (empty if file missing)
if [[ -f "$ENV_FILE" || -p "$ENV_FILE" ]]; then
  ENV_B64=$(base64 < "$ENV_FILE")
else
  echo "No .env file found at ${ENV_FILE}, assuming empty content."
  ENV_B64=$(base64 < /dev/null)
fi

# ENDSSH is deliberately unquoted so ${ENV_B64} expands on the client side.
# All variables that must expand on the remote side are escaped with \$.
# shellcheck disable=SC2087
ssh -T \
  -F "$SSH_CONFIG" \
  -i "$IDENTITY_FILE" \
  "${SSH_USER}@${HOST}" bash << ENDSSH
set -euo pipefail
PROFILE_FILE='/etc/profile.d/tart-provision.sh'
TMPFILE=\$(mktemp)

# Write the shell script header
{
  echo '#!/usr/bin/env bash'
  echo '# Generated by tart provision — do not edit manually'
  echo
} > "\$TMPFILE"

# Decode .env content and convert each key=value line to an export statement
while IFS= read -r line || [[ -n "\$line" ]]; do
  # Trim leading/trailing whitespace
  line="\${line#"\${line%%[![:space:]]*}"}"
  line="\${line%"\${line##*[![:space:]]}"}"

  # Skip empty lines and comments
  [[ -z "\$line" || "\$line" == \#* ]] && continue

  # Split on first '='
  key="\${line%%=*}"
  value="\${line#*=}"

  # Trim whitespace around key
  key="\${key%"\${key##*[![:space:]]}"}"

  # Remove optional surrounding quotes from value
  if [[ "\$value" =~ ^\".*\"\$ ]]; then
    value="\${value:1:-1}"
  elif [[ "\$value" =~ ^\'.*\'\$ ]]; then
    value="\${value:1:-1}"
  fi

  # Validate key (POSIX-like variable name)
  if [[ ! "\$key" =~ ^[A-Za-z_][A-Za-z0-9_]*\$ ]]; then
    echo "Skipping invalid key: \$key" >&2
    continue
  fi

  # Escape value safely and write export statement
  escaped_value=\$(printf '%q' "\$value")
  echo "export \$key=\$escaped_value"
done < <(echo '${ENV_B64}' | base64 -d) >> "\$TMPFILE"

sudo cp "\$TMPFILE" "\$PROFILE_FILE"
sudo chmod 644 "\$PROFILE_FILE"
rm -f "\$TMPFILE"
echo "Updated \$PROFILE_FILE"
ENDSSH

